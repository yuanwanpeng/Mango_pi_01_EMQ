#include "24c0x.h"
#include "IIC.h"

uint16_t G_Delay_Inspection;

//初始化IIC接口
void AT24CXX_Init(void)
{
	IIC_Init(AT24);
}
//在AT24CXX指定地址读出一个数据
//ReadAddr:开始读数的地址
//返回值  :读到的数据
uint8_t AT24CXX_ReadOneByte(uint16_t ReadAddr)
{
	uint8_t temp=0;
    IIC_Start(AT24);
	if(EE_TYPE>AT24C16)
	{
		IIC_Send_Byte(AT24,0XA0);	   //发送写命令
		IIC_Wait_Ack(AT24);
		IIC_Send_Byte(AT24,ReadAddr>>8);//发送高地址
		IIC_Wait_Ack(AT24);
	}else IIC_Send_Byte(AT24,0XA0+((ReadAddr/256)<<1));   //发送器件地址0XA0,写数据

	IIC_Wait_Ack(AT24);
    IIC_Send_Byte(AT24,ReadAddr%256);   //发送低地址
	IIC_Wait_Ack(AT24);
	IIC_Start(AT24);
	IIC_Send_Byte(AT24,0XA1);           //进入接收模式
	IIC_Wait_Ack(AT24);
    temp=IIC_Read_Byte(AT24,0);
    IIC_Stop(AT24);//产生一个停止条件
	return temp;
}

//在AT24CXX指定地址写入一个数据
//WriteAddr  :写入数据的目的地址
//DataToWrite:要写入的数据
void AT24CXX_WriteOneByte(uint16_t WriteAddr,uint8_t DataToWrite)
{
    IIC_Start(AT24);
	if(EE_TYPE>AT24C16)
	{
		IIC_Send_Byte(AT24,0XA0);	    //发送写命令
		IIC_Wait_Ack(AT24);
		IIC_Send_Byte(AT24,WriteAddr>>8);//发送高地址
 	}else
	{
		IIC_Send_Byte(AT24,0XA0+((WriteAddr/256)<<1));   //发送器件地址0XA0,写数据
	}
	IIC_Wait_Ack(AT24);
    IIC_Send_Byte(AT24,WriteAddr%256);   //发送低地址
	IIC_Wait_Ack(AT24);
	IIC_Send_Byte(AT24,DataToWrite);     //发送字节
	IIC_Wait_Ack(AT24);
    IIC_Stop(AT24);//产生一个停止条件
    osDelay(10);
}
//在AT24CXX里面的指定地址开始写入长度为Len的数据
//该函数用于写入16bit或者32bit的数据.
//WriteAddr  :开始写入的地址
//DataToWrite:数据数组首地址
//Len        :要写入数据的长度2,4
void AT24CXX_WriteLenByte(uint16_t WriteAddr,uint32_t DataToWrite,uint8_t Len)
{
	uint8_t t;
	for(t=0;t<Len;t++)
	{
		AT24CXX_WriteOneByte(WriteAddr+t,(DataToWrite>>(8*t))&0xff);
	}
}

//在AT24CXX里面的指定地址开始读出长度为Len的数据
//该函数用于读出16bit或者32bit的数据.
//ReadAddr   :开始读出的地址
//返回值     :数据
//Len        :要读出数据的长度2,4
uint32_t AT24CXX_ReadLenByte(uint16_t ReadAddr,uint8_t Len)
{
	uint8_t t;
	uint32_t temp=0;
	for(t=0;t<Len;t++)
	{
		temp<<=8;
		temp+=AT24CXX_ReadOneByte(ReadAddr+Len-t-1);
	}
	return temp;
}
//检查AT24CXX是否正常
//这里用了24XX的最后一个地址(255)来存储标志字.
//如果用其他24C系列,这个地址要修改
//返回1:检测失败
//返回0:检测成功
uint8_t AT24CXX_Check(uint8_t hardware)
{
	uint8_t temp;
	temp=AT24CXX_ReadOneByte(255);//避免每次开机都写AT24CXX
	if(temp==0X55)return 0;
	else//排除第一次初始化的情况
	{
		AT24CXX_WriteOneByte(255,0X55);
	    temp=AT24CXX_ReadOneByte(255);
		if(temp==0X55)return 0;
	}
	return 1;
}

//在AT24CXX里面的指定地址开始读出指定个数的数据
//ReadAddr :开始读出的地址 对24c02为0~255
//pBuffer  :数据数组首地址
//NumToRead:要读出数据的个数
void AT24CXX_Read(uint16_t ReadAddr,uint8_t *pBuffer,uint16_t NumToRead)
{
	while(NumToRead)
	{
		*pBuffer++=AT24CXX_ReadOneByte(ReadAddr++);
		NumToRead--;
	}
}
//在AT24CXX里面的指定地址开始写入指定个数的数据
//WriteAddr :开始写入的地址 对24c02为0~255
//pBuffer   :数据数组首地址
//NumToWrite:要写入数据的个数
void AT24CXX_Write(uint16_t WriteAddr,uint8_t *pBuffer,uint16_t NumToWrite)
{
	while(NumToWrite--)
	{
		AT24CXX_WriteOneByte(WriteAddr,*pBuffer);
		WriteAddr++;
		pBuffer++;
	}
	
}

void Check_Delay_Inspection(void)
{
	uint8_t Delay_Inspection_H = 0,Delay_Inspection_L = 0;
	uint16_t Delay_Inspection;
	AT24CXX_Read(DELAY_INSPECTION,&Delay_Inspection_H,sizeof(Delay_Inspection_H));
	AT24CXX_Read(DELAY_INSPECTION+1,&Delay_Inspection_L,sizeof(Delay_Inspection_L));
	Delay_Inspection = (Delay_Inspection_H<<8)|Delay_Inspection_L;
	if(Delay_Inspection>600)
	{
		Init_Delay_Inspection(0);
	}else{
		G_Delay_Inspection = Delay_Inspection;
	}
}
void Init_Delay_Inspection(uint16_t Delay_Inspection)
{
	uint8_t Delay_Inspection_H,Delay_Inspection_L;
	Delay_Inspection_H = ((Delay_Inspection>>8)&0x00FF);
	Delay_Inspection_L = (Delay_Inspection&0x00FF);
	AT24CXX_Write(DELAY_INSPECTION,&Delay_Inspection_H,sizeof(Delay_Inspection_H));
	AT24CXX_Write(DELAY_INSPECTION+1,&Delay_Inspection_L,sizeof(Delay_Inspection_L));

	G_Delay_Inspection = Delay_Inspection;
}
